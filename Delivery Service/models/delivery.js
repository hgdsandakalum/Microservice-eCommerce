const mongoose = require('mongoose');

const deliverySchema = new mongoose.Schema({
    customerId: {
        type: String,
        required: true,
    },
    firstName: {
        type: String,
        required: true,
    },
    lastName: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    phone: {
        type: String,
        required: true,
    },
    address: {
        type: String,
        required: true,
    }
}, { timestamps: true });

module.exports = mongoose.model('Delivery', deliverySchema);